    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        double utilidadbruta = Double.parseDouble(jTextField4.getText());
        double costosoperativos = Double.parseDouble(jTextField5.getText());
        double depreciaciondeequipo = Double.parseDouble(jTextField6.getText());
        double gastosoperativos = utilidadbruta - (costosoperativos + depreciaciondeequipo);
        jTextField7.setText(String.valueOf(gastosoperativos));
    }//GEN-LAST:event_jButton3ActionPerformed

