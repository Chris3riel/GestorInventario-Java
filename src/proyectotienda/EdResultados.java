    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        double utilidadesnetas = Double.parseDouble(jTextField9.getText());
        double impuestosapagar = Double.parseDouble(jTextField10.getText());
        double operacion = (utilidadesnetas * impuestosapagar)/100;
        double utilidadneta = utilidadesnetas - operacion;
        jTextField11.setText(String.valueOf(utilidadneta));
    }//GEN-LAST:event_jButton5ActionPerformed


